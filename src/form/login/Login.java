    private void loginButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginButtonActionPerformed
        String userName = username.getText();
        String passWord =  new String(password.getPassword());
        boolean loginCheck = false;
        List<Librarian> librarianList = getLibrarianList();
        for (Librarian lb : librarianList) {
            if (lb.getUsername().equals(userName) && lb.getPassword().equals(passWord)) {
                loginCheck = true;
                Admin = lb;
            }      
        }
        if (loginCheck) {
            this.dispose();
            DashBoard db = new DashBoard();
            db.setVisible(true);
            JOptionPane.showMessageDialog(this, "Đăng nhập thành công!");
        } else {
            JOptionPane.showMessageDialog(this, "Tên đăng nhập hoặc mật khẩu sai!");
        }
    }//GEN-LAST:event_loginButtonActionPerformed

